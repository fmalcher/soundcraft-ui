import { DBToFaderValue, faderValueToDB } from './value-converters';

describe('Value converters', () => {
  it('should convert linear value to dB value', () => {
    expect(faderValueToDB(0.98056922374235)).toMatchInlineSnapshot(`9`);
    expect(faderValueToDB(0.9165390820271568)).toMatchInlineSnapshot(`6`);
    expect(faderValueToDB(0.8932095183699857)).toMatchInlineSnapshot(`5`);
    expect(faderValueToDB(0.8689202935784124)).toMatchInlineSnapshot(`4`);
    expect(faderValueToDB(0.8437562720209826)).toMatchInlineSnapshot(`3`);
    expect(faderValueToDB(0.8178584068082273)).toMatchInlineSnapshot(`2`);
    expect(faderValueToDB(0.7914255296927877)).toMatchInlineSnapshot(`1`);
    expect(faderValueToDB(0.7647058823495172)).toMatchInlineSnapshot(`0`);
    expect(faderValueToDB(0.7379774276050739)).toMatchInlineSnapshot(`-0.9`);
    expect(faderValueToDB(0.7115203608991578)).toMatchInlineSnapshot(`-1.9`);
    expect(faderValueToDB(0.6855889220023528)).toMatchInlineSnapshot(`-2.9`);
    expect(faderValueToDB(0.612728497479111)).toMatchInlineSnapshot(`-5.9`);
    expect(faderValueToDB(0.5487738897791132)).toMatchInlineSnapshot(`-8.9`);
    expect(faderValueToDB(0.4933368943165988)).toMatchInlineSnapshot(`-11.9`);
    expect(faderValueToDB(0.4450032153399661)).toMatchInlineSnapshot(`-14.9`);
    expect(faderValueToDB(0.4023566844407469)).toMatchInlineSnapshot(`-17.9`);
    expect(faderValueToDB(0.37650583661161363)).toMatchInlineSnapshot(`-19.9`);
    expect(faderValueToDB(0.2693554200232029)).toMatchInlineSnapshot(`-29.9`);
    expect(faderValueToDB(0.11764705926179886)).toMatchInlineSnapshot(`-49.9`);
    expect(faderValueToDB(0.05882352590560913)).toMatchInlineSnapshot(`-59.9`);
    expect(faderValueToDB(0.025155186653137207)).toMatchInlineSnapshot(`-69.9`);
    expect(faderValueToDB(0.010385841131210327)).toMatchInlineSnapshot(`-79.9`);
    expect(faderValueToDB(0.003787517547607422)).toMatchInlineSnapshot(`-89.9`);
    expect(faderValueToDB(0.001269415020942688)).toMatchInlineSnapshot(`-99.9`);
    expect(faderValueToDB(0)).toMatchInlineSnapshot(`-Infinity`);
  });

  it('should convert dB value to linear value', () => {
    expect(DBToFaderValue(10)).toMatchInlineSnapshot(`1`);
    expect(DBToFaderValue(9)).toMatchInlineSnapshot(`0.98056922374235`);
    expect(DBToFaderValue(6)).toMatchInlineSnapshot(`0.9165390820271568`);
    expect(DBToFaderValue(5)).toMatchInlineSnapshot(`0.8932095183699857`);
    expect(DBToFaderValue(4)).toMatchInlineSnapshot(`0.8689202935784124`);
    expect(DBToFaderValue(3)).toMatchInlineSnapshot(`0.8437562720209826`);
    expect(DBToFaderValue(2)).toMatchInlineSnapshot(`0.8178584068082273`);
    expect(DBToFaderValue(1)).toMatchInlineSnapshot(`0.7914255296927877`);
    expect(DBToFaderValue(0)).toMatchInlineSnapshot(`0.7647058823495172`);
    expect(DBToFaderValue(-1)).toMatchInlineSnapshot(`0.7379774276050739`);
    expect(DBToFaderValue(-2)).toMatchInlineSnapshot(`0.7115203608991578`);
    expect(DBToFaderValue(-3)).toMatchInlineSnapshot(`0.6855889220023528`);
    expect(DBToFaderValue(-6)).toMatchInlineSnapshot(`0.612728497479111`);
    expect(DBToFaderValue(-9)).toMatchInlineSnapshot(`0.5487738897791132`);
    expect(DBToFaderValue(-12)).toMatchInlineSnapshot(`0.4933368943165988`);
    expect(DBToFaderValue(-15)).toMatchInlineSnapshot(`0.4450032153399661`);
    expect(DBToFaderValue(-18)).toMatchInlineSnapshot(`0.4023566844407469`);
    expect(DBToFaderValue(-20)).toMatchInlineSnapshot(`0.37650583661161363`);
    expect(DBToFaderValue(-30)).toMatchInlineSnapshot(`0.2693554200232029`);
    expect(DBToFaderValue(-50)).toMatchInlineSnapshot(`0.11764705926179886`);
    expect(DBToFaderValue(-60)).toMatchInlineSnapshot(`0.05882352590560913`);
    expect(DBToFaderValue(-70)).toMatchInlineSnapshot(`0.025155186653137207`);
    expect(DBToFaderValue(-80)).toMatchInlineSnapshot(`0.010385841131210327`);
    expect(DBToFaderValue(-90)).toMatchInlineSnapshot(`0.003787517547607422`);
    expect(DBToFaderValue(-100)).toMatchInlineSnapshot(`0.001269415020942688`);
    expect(DBToFaderValue(-Infinity)).toMatchInlineSnapshot(`0`);
  });
});
